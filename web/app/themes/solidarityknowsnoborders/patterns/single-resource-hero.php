<p>This workbook was originally created as a resource for the Solidarity Knows No Borders Community of Resistance, which is fighting for migrant justice and dignity for all.<br>The first version was made in the run up to a week of action in 2021 where over 60 groups came together across the country to take action against the hostile environment immigration system.<br>In order to create the world we want to win, we must build our power as a progressive movement for social justice that does not leave anyone behind.<br>This toolkit is for you to use, adapt and share with your community. In the spirit of learning together, we recognise that this is not a definitive guide.<br>We have learned from organisers of the past and continue to learn from comrades, friends and organisers today.<br>You will find a list of some SKNB members at the back of this guide.</p>
